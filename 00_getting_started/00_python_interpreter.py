
## you can type "python3" in the terminal to start the Python interpreter. You can also type "python" if you are using Windows or if you have set up an alias for Python 3.

## Once you are in the Python interpreter, you can type Python code and see the results immediately. For example, you can type `print("Hello, World!")` and press Enter to see the output.

## You can exit the Python interpreter by typing `exit()` or pressing Ctrl+D (on Linux/Mac) or Ctrl+Z followed by Enter (on Windows).

## Here are some basic commands you can try in the Python interpreter:
# 1. Print a message
print("Hello, World!")
# 2. Perform a simple calculation
result = 2 + 2
print(result)
# 3. Create a variable and print its value
name = "Alice"
print(name)
# 4. Use a loop to print numbers from 1 to 5
for i in range(1, 6):
    print(i)
# 5. Check a condition 
2 > 5